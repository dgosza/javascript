var n = 2;

// == COMPARA A DIFERENCA ENTRE O VALOR CONTIDO ENTRE A STRING 
//    E O NUMERO. O RESULTADO SERA TRUE POIS SERA COMPARADO O VALOR DO MESMO.
if (n == '2') {
    console.log('true')
} else {
    console.log('false')
}

// === COMPARA O TIPO DA VARIAVEL E O VALOR A SER COMPARADO. 
//     NESTE CASO SERA FALSE POIS STRING EH DIFERENTE DE NUMBER
if (n === '2') {
    console.log('true')
} else {
    console.log('false')
}

